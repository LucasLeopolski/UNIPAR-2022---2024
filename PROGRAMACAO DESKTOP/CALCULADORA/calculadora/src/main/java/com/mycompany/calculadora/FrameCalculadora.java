    public void operacaoAritimetica(){
        switch (calculo) {
            
            case 1: // adição
                    operacao =  num + Double.parseDouble(jTextField2.getText()); //convertendo String para Double para fazer o calculo 
                    jTextField1.setText(Double.toString(operacao)); // convertendo o resultado do calculo, contino na variavel operacao em String novamente para apresentar na tela do usuario  
                break;
                
            case 2: // subtração
                
                operacao =  num - Double.parseDouble(jTextField2.getText()); //convertendo String para Double    
                    jTextField1.setText(Double.toString(operacao));
                break;
             
            case 3: // multiplicação
                
                operacao = num * Double.parseDouble(jTextField2.getText());
                jTextField2.setText(Double.toString(operacao));
                break;
                
            case 4: // divisão
                
                operacao = num / Double.parseDouble(jTextField2.getText());
                jTextField2.setText(Double.toString(operacao));
                break;
                
        }
        
    
    }
    

