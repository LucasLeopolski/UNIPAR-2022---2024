    private void btLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLimparActionPerformed
        
        int length = jTextField2.getText().length();    // variavel usada para saber o comprimento da String através do método length
        int num = jTextField2.getText().length() -1; // nessa variavél vai pegar o satmanho da string e subtrair 1 
        String valorDigitado;
        
        if (length > 0) {
        
            StringBuilder apagar = new StringBuilder(jTextField2.getText()); //StringBuilder cria um construtor de strings vazio parar armazenar Strings
            apagar.deleteCharAt(num); // usado para deletar um unico caractere de uma posição expecífica
            valorDigitado= apagar.toString(); // convertendo novamente para String
            jTextField2.setText(valorDigitado); // o valor do campo de texto é armazenado na variavél "valorDigitado"
        }
    }//GEN-LAST:event_btLimparActionPerformed

