    public void atualizaGrid(){
        try {
            String sql = "SELECT * FROM public.\"Cliente\" ORDER BY \"ID_CLIENTE\";";
            lista = new ArrayList<>();
            lista = clienteDao.retornarLista(sql);
            
            tbClientes.removeAll();
            
            DefaultTableModel tableModel = new DefaultTableModel(
            new Object [][]{},
            new String []{"ID", "NOME","EMAIL","TELEFONE"}){
            @Override
            public boolean isCellEditable(int row, int column){
            return false;
            }
            };
            
            tbClientes.setModel(tableModel);
            
            DefaultTableModel dm = (DefaultTableModel)tbClientes.getModel();
            
            for(Cliente cliente: lista){
                dm.addRow(new Object[]{cliente.getId(), cliente.getNome(), cliente.getEmail(), cliente.getTelefone()});
            
            }
            
            tbClientes.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    int linhaSelecionada = tbClientes.getSelectedRow();
                    if(linhaSelecionada != -1){
                        mostrarCliente(lista.get(linhaSelecionada));}
                
                }
            
            });
        } catch (Exception ex) {
        }
    }
    
    public void limparCampos(){
    tfNome.setText("");
    tfEmail.setText("");
    tfTelefone.setText("");
    tfID.setEditable(true);
    btCadastrarCleinte.setEnabled(false);
    }
    
    
    public void mostrarCliente(Cliente cliente){
    tfID.setText(String.valueOf(cliente.getId()));
    tfNome.setText(cliente.getNome());
    tfEmail.setText(cliente.getEmail());
    tfTelefone.setText(cliente.getTelefone());
    tfID.setEditable(false);
    btCadastrarCleinte.setEnabled(false);
    clientePesquisado = true;
    }

